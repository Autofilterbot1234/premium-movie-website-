async def search_movie_links(query: str):
    """
    ‡¶á‡¶®‡ßç‡¶ü‡¶æ‡¶∞‡¶®‡ßá‡¶ü‡ßá ‡¶∏‡¶æ‡¶∞‡ßç‡¶ö ‡¶ï‡¶∞‡ßá ‡¶Æ‡ßÅ‡¶≠‡¶ø‡¶∞ ‡¶≤‡¶ø‡¶ô‡ßç‡¶ï ‡¶ñ‡ßÅ‡¶Å‡¶ú‡ßá ‡¶¨‡ßá‡¶∞ ‡¶ï‡¶∞‡¶æ‡¶∞ ‡¶´‡¶æ‡¶Ç‡¶∂‡¶®‡•§
    ‡¶è‡¶ü‡¶ø ‡¶¨‡¶ø‡¶≠‡¶ø‡¶®‡ßç‡¶® ‡¶∏‡¶æ‡¶∞‡ßç‡¶ö ‡¶ü‡¶æ‡¶∞‡ßç‡¶Æ ‡¶¨‡ßç‡¶Ø‡¶¨‡¶π‡¶æ‡¶∞ ‡¶ï‡¶∞‡ßá ‡¶∏‡ßá‡¶∞‡¶æ ‡¶´‡¶≤‡¶æ‡¶´‡¶≤ ‡¶ñ‡ßã‡¶Å‡¶ú‡¶æ‡¶∞ ‡¶ö‡ßá‡¶∑‡ßç‡¶ü‡¶æ ‡¶ï‡¶∞‡ßá‡•§
    """
    search_query = f'"{query}" (480p OR 720p OR 1080p) "Google Drive" -site:youtube.com'
    url = f"https://www.google.com/search?q={search_query}"
    
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
    }
    
    links = {}
    try:
        response = await asyncio.to_thread(requests.get, url, headers=headers)
        response.raise_for_status()
        
        soup = BeautifulSoup(response.text, 'html.parser')
        
        for a_tag in soup.find_all('a', href=True):
            href = a_tag.get('href')
            if href.startswith('/url?q='):
                link = href.split('/url?q=')[1].split('&sa=')[0]
                if 'drive.google.com' in link:
                    # ‡¶∞‡ßá‡¶ú‡ßã‡¶≤‡¶ø‡¶â‡¶∂‡¶® ‡¶∏‡¶®‡¶æ‡¶ï‡ßç‡¶§ ‡¶ï‡¶∞‡¶æ‡¶∞ ‡¶ö‡ßá‡¶∑‡ßç‡¶ü‡¶æ
                    text = a_tag.get_text().lower()
                    resolution = "Unknown"
                    if "1080p" in text:
                        resolution = "1080p"
                    elif "720p" in text:
                        resolution = "720p"
                    elif "480p" in text:
                        resolution = "480p"
                    
                    if resolution not in links: # ‡¶™‡ßç‡¶∞‡¶§‡¶ø ‡¶∞‡ßá‡¶ú‡ßã‡¶≤‡¶ø‡¶â‡¶∂‡¶®‡ßá ‡¶è‡¶ï‡¶ü‡¶ø ‡¶≤‡¶ø‡¶ô‡ßç‡¶ï ‡¶∞‡¶æ‡¶ñ‡¶æ
                        links[resolution] = link
                        if len(links) >= 5: # ‡¶∏‡¶∞‡ßç‡¶¨‡ßã‡¶ö‡ßç‡¶ö ‡ß´‡¶ü‡¶ø ‡¶≤‡¶ø‡¶ô‡ßç‡¶ï
                            break
                            
    except requests.exceptions.RequestException as e:
        logger.error(f"Google search failed: {e}")
    except Exception as e:
        logger.error(f"An unexpected error in search_movie_links: {e}")
        
    return links

